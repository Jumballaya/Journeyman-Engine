func init() {
	generateScriptCmd.Flags().StringP("output", "o", "", "Output directory for the script (default: assets/scripts)")
	generateSceneCmd.Flags().StringP("output", "o", "", "Output directory for the scene (default: scenes)")
	generateAtlasCmd.Flags().StringP("output", "o", "", "Output directory for the atlas (default: assets/atlases)")
	generateManifestCmd.Flags().StringP("output", "o", ".jm.json", "Output path for the manifest (default: .jm.json)")

	generateCmd.AddCommand(generateScriptCmd)
	generateCmd.AddCommand(generateSceneCmd)
	generateCmd.AddCommand(generateAtlasCmd)
	generateCmd.AddCommand(generateManifestCmd)
}

func generateScript(name, outputDir string) error {
	// Sanitize name
	name = strings.ToLower(strings.ReplaceAll(name, " ", "_"))

	// Create output directory
	if err := os.MkdirAll(outputDir, os.ModePerm); err != nil {
		return fmt.Errorf("failed to create output directory: %w", err)
	}

	// Generate TypeScript file
	tsPath := filepath.Join(outputDir, name+".ts")
	tsContent := fmt.Sprintf(`// %s script
// Generated by Journeyman CLI

let t: f32 = 0;

export function onUpdate(dt: f32): void {
    t += dt;
}

export function onStart(): void {
    // Initialize script here
}
`, strings.Title(strings.ReplaceAll(name, "_", " ")))

	if err := os.WriteFile(tsPath, []byte(tsContent), 0644); err != nil {
		return fmt.Errorf("failed to write TypeScript file: %w", err)
	}

	// Generate script manifest
	manifestPath := filepath.Join(outputDir, name+".script.json")
	manifestContent := fmt.Sprintf(`{
  "name": "%s",
  "script": "%s/%s.ts",
  "binary": "%s/%s.wasm"
}
`, strings.Title(strings.ReplaceAll(name, "_", " ")), outputDir, name, outputDir, name)

	if err := os.WriteFile(manifestPath, []byte(manifestContent), 0644); err != nil {
		return fmt.Errorf("failed to write script manifest: %w", err)
	}

	return nil
}

func generateScene(name, outputDir string) error {
	// Sanitize name
	name = strings.ToLower(strings.ReplaceAll(name, " ", "_"))

	// Create output directory
	if err := os.MkdirAll(outputDir, os.ModePerm); err != nil {
		return fmt.Errorf("failed to create output directory: %w", err)
	}

	// Generate scene JSON
	scenePath := filepath.Join(outputDir, name+".scene.json")
	sceneContent := fmt.Sprintf(`{
  "name": "%s",
  "entities": [
    {
      "name": "Example Entity",
      "components": {
        "ScriptComponent": {
          "script": "assets/scripts/example"
        }
      }
    }
  ]
}
`, strings.Title(strings.ReplaceAll(name, "_", " ")))

	if err := os.WriteFile(scenePath, []byte(sceneContent), 0644); err != nil {
		return fmt.Errorf("failed to write scene file: %w", err)
	}

	return nil
}

func generateAtlas(name, outputDir string) error {
	// Sanitize name
	name = strings.ToLower(strings.ReplaceAll(name, " ", "_"))

	// Create output directory
	if err := os.MkdirAll(outputDir, os.ModePerm); err != nil {
		return fmt.Errorf("failed to create output directory: %w", err)
	}

	// Generate atlas configuration
	atlasPath := filepath.Join(outputDir, name+".atlas.json")
	atlasContent := fmt.Sprintf(`{
  "name": "%s",
  "output": "assets/atlases/%s.png",
  "outputMetadata": "assets/atlases/%s.atlas.json",
  "maxSize": 2048,
  "padding": 2,
  "sprites": [
    {
      "name": "sprite1",
      "path": "assets/sprites/sprite1.png"
    }
  ]
}
`, strings.Title(strings.ReplaceAll(name, "_", " ")), name, name)

	if err := os.WriteFile(atlasPath, []byte(atlasContent), 0644); err != nil {
		return fmt.Errorf("failed to write atlas config: %w", err)
	}

	return nil
}

func generateManifest(name, outputPath string) error {
	// Sanitize name
	gameName := strings.Title(strings.ReplaceAll(name, "_", " "))

	// Generate manifest JSON
	manifestContent := fmt.Sprintf(`{
  "name": "%s",
  "version": "0.1.0",
  "engine": "../build/engine/journeyman_engine",
  "entryScene": "scenes/level1.scene.json",
  "scenes": [
    "scenes/level1.scene.json"
  ],
  "assets": [
  ]
}
`, gameName)

	if err := os.WriteFile(outputPath, []byte(manifestContent), 0644); err != nil {
		return fmt.Errorf("failed to write manifest file: %w", err)
	}

	return nil
}
