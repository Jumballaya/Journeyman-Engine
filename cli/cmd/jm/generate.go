package main

import (
	"fmt"
	"os"
	"path/filepath"
	"strings"

	"github.com/spf13/cobra"
)

var generateCmd = &cobra.Command{
	Use:   "generate",
	Short: "Generate asset skeletons and templates",
	Long:  "Generate asset skeletons for scripts, scenes, atlases, and other game assets",
}

var generateScriptCmd = &cobra.Command{
	Use:   "script [name]",
	Short: "Generate a new script asset",
	Long:  "Generate a new TypeScript script with its corresponding .script.json manifest",
	Args:  cobra.ExactArgs(1),
	Run: func(cmd *cobra.Command, args []string) {
		name := args[0]
		outputDir, _ := cmd.Flags().GetString("output")
		if outputDir == "" {
			outputDir = "assets/scripts"
		}

		if err := generateScript(name, outputDir); err != nil {
			fmt.Printf("Error generating script: %s\n", err)
			os.Exit(1)
		}

		fmt.Printf("Generated script: %s/%s.ts and %s/%s.script.json\n", outputDir, name, outputDir, name)
	},
}

